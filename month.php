function toDateThai( $d ) {
    $m = array(
        "",
        "มกราคม",
        "กุมภาพันธ์",
        "มีนาคม",
        "เมษายน",
        "พฤษภาคม",
        "มิถุนายน",
        "กรกฎาคม",
        "สิงหาคม",
        "กันยายน",
        "ตุลาคม",
        "พฤศจิกายน",
        "ธันวาคม"
    );
    $e = explode("/", $d);
    $r = $e[0]." ".$m[(int)$e[1]]." ".($e[2]+543);        
    return $r;
}

